        if (combo1.getSelectedItem()=="---pilih---"){
            H1.setText("");
            H2.setText("");
            H1.setEnabled(false);
            H2.setEnabled(false);
            combo1.requestFocus();
        }else {
            if(combo1.getSelectedItem()=="Sawah"){
                harga = "125000";

            }else if (combo1.getSelectedItem()=="Pegununggan"){
                harga = "1200000";
            }else if (combo1.getSelectedItem()=="Perkotaan"){
                harga = "1500000";
            }else if (combo1.getSelectedItem()=="Ladang"){
                harga = "500000";
            }else {
                harga = "3000000";
            }
            H1.setText(harga);
        }
