    void Data(){
        Data.setText("");
        
        String bang = "";
        
        try {
            Scanner file = new Scanner(new File(kira));
            while(file.hasNext()){
                bang += file.nextLine();
//                System.out.println(bang);
//                Data.setText(bang);
            }
            String bro[] = bang.split("+");
            
            for(int i =0 ;i < bro.length;i++){
                Data.append(bro[i] + " \n ");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

